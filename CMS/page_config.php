    function leitura_dados(){
        $.get('website/receber.php', function(data){
            // Converte a resposta JSON em objeto JavaScript
            var dados = JSON.parse(data);
            // Define os valores dos campos com base nos dados recebidos
            $('#address').val(dados[0].address);
            $('#latitude').val(dados[0].latitude);
            $('#longitude').val(dados[0].longitude);
            $('#contacts').val(dados[0].contact);
            $('#facebook').val(dados[0].facebook);
            $('#twitter').val(dados[0].twitter);
            $('#linkedin').val(dados[0].linkedin);
            $('#youtube').val(dados[0].youtube);
            // Você pode definir mais campos aqui se necessário
        });
    }

$(document).ready(function(){

    leitura_dados();
    // Observa as mudanças nos toggles
    $(".custom-control-input").change(function(){
        // Pega o ID do toggle que foi alterado
        var toggleId = $(this).attr("id");
        // Pega o ID do campo de entrada correspondente
        var inputId = toggleId.replace("Toggle", "Input");
        // Mostra ou oculta o campo de entrada correspondente com base no estado do toggle
        if ($(this).is(":checked")) {
            $("#" + inputId).show();
        } else {
            $("#" + inputId).hide();
        }
    });

    // Intercepta o evento de envio do formulário
    $("form").submit(function(event){
        // Previne o envio padrão do formulário
        event.preventDefault();
        
        // Serializa os dados do formulário
        var formData = $(this).serialize();
        
        // Envia os dados usando AJAX
        $.ajax({
            type: "POST",
            url: "website/enviar.php", // Substitua 'seu_arquivo_de_destino.php' pelo URL do arquivo de destino
            data: formData,
            success: function(response){
                // Manipule a resposta do servidor, se necessário
                console.log(response);
                alert("Formulário enviado com sucesso!");
            },
            error: function(){
                // Manipule erros de requisição, se necessário
                alert("Ocorreu um erro ao enviar o formulário.");
            }
        });
    });
});
